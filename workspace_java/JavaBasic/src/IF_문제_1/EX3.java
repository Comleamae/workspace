package IF_문제_1;

public class EX3 {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        if (a > b) {
            System.out.println("a가 큽니다");
        }
        else if (b > a) {
            System.out.println("b가 큽니다");
        }
        else if (a == b) {
            System.out.println("같습니다");
        }
    }
}
